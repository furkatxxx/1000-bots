async function main() {
  // Создаём тестовый отчёт
  const report = await prisma.dailyReport.create({
    data: {
      date: new Date("2026-03-02T12:00:00Z"),
      status: "complete",
      trendsCount: 0,
      ideasCount: 4,
      aiModel: "manual-test",
      generatedAt: new Date(),
    },
  });

  console.log(`Отчёт создан: ${report.id}`);

  // Добавляем 4 варианта
  for (const v of VARIANTS) {
    const idea = await prisma.businessIdea.create({
      data: {
        reportId: report.id,
        name: v.name,
        emoji: v.emoji,
        description: v.description,
        targetAudience: v.targetAudience,
        monetization: v.monetization,
        startupCost: v.startupCost,
        competitionLevel: v.competitionLevel,
        trendBacking: v.trendBacking,
        actionPlan: v.actionPlan,
        difficulty: v.difficulty,
        successChance: v.successChance,
        estimatedRevenue: v.estimatedRevenue,
        timeToLaunch: v.timeToLaunch,
        market: v.market,
        claudeCodeReady: true,
      },
    });
    console.log(`  Идея: ${idea.name} → ${idea.id}`);
  }

  console.log("\nГотово! 4 варианта добавлены.");
  await prisma.$disconnect();
}

main().catch(console.error);
