// Все ID пресетов для валидации
export const PRESET_IDS = Object.keys(FOCUS_PRESETS) as PresetId[];
export const MAX_PRESETS = 3;

// ═══════════════════════════════════════════════════
// ПРОМПТ-БИЛДЕР: строит фокусный блок для AI
// ═══════════════════════════════════════════════════

export function buildFocusBlock(presetIds: PresetId[]): string {
  // Валидация: только известные пресеты, макс 3
  const valid = presetIds.filter((id) => id in FOCUS_PRESETS).slice(0, MAX_PRESETS);

  if (valid.length === 0) {
    return ""; // универсальный режим — без фокуса
  }

  if (valid.length === 1) {
    // Один пресет → глубокий фокус
    return FOCUS_PRESETS[valid[0]].deepPrompt;
  }

  // 2-3 пресета → поиск на стыке
  const dirs = valid
    .map((id) => {
      const p = FOCUS_PRESETS[id];
      return `- ${p.emoji} **${p.name}**: ${p.keywords}`;
    })
    .join("\n");

  const examples = generateCrossExamples(valid);

  return `## Фокус генерации: пересечение направлений

Активные направления:
${dirs}

⚡ ГЛАВНОЕ ПРАВИЛО: ищи идеи НА СТЫКЕ этих направлений.
Не "отдельно из каждого", а продукты которые ОБЪЕДИНЯЮТ ${valid.length} мира.
Чем неочевиднее пересечение — тем ценнее идея.

${examples}

Каждая идея должна задействовать минимум 2 из ${valid.length} направлений.
Идеи, которые относятся только к одному направлению — НЕ подходят.`;
}

// Генерация примеров пересечений для конкретной комбинации
function generateCrossExamples(presetIds: PresetId[]): string {
  const examples: string[] = [];

  const has = (id: PresetId) => presetIds.includes(id);

  if (has("marketplace") && has("telegram")) {
    examples.push("Telegram-бот для селлеров WB: уведомления о просадке позиций + автоответы на отзывы");
  }
  if (has("marketplace") && has("content")) {
    examples.push("AI-генератор SEO-описаний карточек товаров для Ozon из фото");
  }
  if (has("marketplace") && has("automation")) {
    examples.push("Автопарсер закупочных цен + калькулятор маржи для селлеров");
  }
  if (has("marketplace") && has("microsaas")) {
    examples.push("SaaS-dashboard аналитики продаж на WB с глобальным рынком (Shopify, Etsy)");
  }
  if (has("telegram") && has("content")) {
    examples.push("Telegram-бот генерации контент-планов и постов для каналов");
  }
  if (has("telegram") && has("automation")) {
    examples.push("Telegram-бот автообработки заявок: клиент пишет → бот классифицирует → создаёт задачу");
  }
  if (has("telegram") && has("microsaas")) {
    examples.push("Telegram Mini App как интерфейс для нишевого SaaS-инструмента");
  }
  if (has("content") && has("automation")) {
    examples.push("Автогенерация отчётов для клиентов агентства: данные из CRM → красивый PDF");
  }
  if (has("content") && has("microsaas")) {
    examples.push("Глобальный SaaS для автогенерации продуктовых описаний под SEO");
  }
  if (has("automation") && has("microsaas")) {
    examples.push("Нишевой SaaS для автоматизации одного бизнес-процесса (например, invoice → 1С)");
  }

  if (examples.length === 0) return "";

  return `Примеры пересечений (для вдохновения, НЕ копировать):\n${examples.map((e) => `- ${e}`).join("\n")}`;
}

// Парсинг JSON из БД → массив PresetId
export function parsePresets(json: string | null | undefined): PresetId[] {
  if (!json) return [];
  try {
    const arr = JSON.parse(json);
    if (!Array.isArray(arr)) return [];
    return arr.filter((id: string) => id in FOCUS_PRESETS).slice(0, MAX_PRESETS) as PresetId[];
  } catch {
    return [];
  }
}
